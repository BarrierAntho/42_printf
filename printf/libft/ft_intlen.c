#include <string.h>

/* Get length of integer. At minimum i = 1 because 0 <= n <= 9 */
size_t	ft_intlen(int n)
{
	size_t	i;

	i = 1;
	if (n < 0)
	{
		n = -n;
		i++;
	}
	while (n / 10 > 0)
	{
		n /= 10;
		i++;
	}
	return (i);
}
